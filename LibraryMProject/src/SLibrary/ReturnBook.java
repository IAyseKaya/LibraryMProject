    private void txtmidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmidKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
           
            String mid= txtmid.getText();
            
            try {
                pst = con.prepareStatement("select m.name , b.bname , i.returndate ,DATEDIFF (now(),i.returndate) as elap from issuebook i JOIN member m ON i.mid = m.id JOIN book b ON i.bookid = b.id and i.mid = ? ");
                pst.setString(1 , mid );
                rs = pst.executeQuery();
                
                if(rs.next() == false){
                    JOptionPane.showMessageDialog(this , "Member ID not found.");
                }
                else{
                    String mname = rs.getString("m.name");
                    String bname = rs.getString("b.bname");
                    String rdate = rs.getString("i.returndate");
                    
                    String elp = rs.getString("elap");
                    
                    txtmname.setText(mname.trim());
                    txtbook.setText(bname.trim());
                    txtreturndate.setText(rdate.trim());
                    
                    int elaped = Integer.parseInt(elp);
                    
                    if(elaped > 0){
                        
                        txtdelap.setText(elp);
                        int fine = elaped * 100;
                        txtfine.setText(String.valueOf(fine));
                        
                    }
                    else{
                        txtdelap.setText("0");
                        txtfine.setText("0");
                    }
                }
                
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(ReturnBook.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            
            
        }
        
    }//GEN-LAST:event_txtmidKeyPressed

